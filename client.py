def drawSprite(win, player):
    if player.character == "firedude":
        image = pygame.image.load("./assets/sprites/firedude.png")
        image = pygame.transform.scale(image, (32,58))
        image.set_colorkey((255, 255, 255))
    else:
        image = pygame.image.load("./assets/sprites/waterchick.png")
        image = pygame.transform.scale(image, (32,58))
        image.set_colorkey((255, 255, 255))      

    if player.orientation == "right":
        win.blit(image, player.hitbox)
    elif player.orientation == "left":
        win.blit(pygame.transform.flip(
            image, True, False), player.hitbox)

def redrawWindow(win, player, player2):
    win.fill((255, 255, 255))
    player.draw(win)
    player2.draw(win)
    drawSprite(win, player)
    drawSprite(win, player2)
    pygame.display.update()

def main():
    run = True
    n = Network()
    p = n.getP()
    clock = pygame.time.Clock()
    world = World(world_map, (107, 95, 55))
    while run:
        clock.tick(60)
        p2 = n.send(p)

        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                run = False
                pygame.quit()

        p.move(world)
        redrawWindow(win, p, p2)
        world.draw(win)


main()
